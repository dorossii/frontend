enum CharacterAnimationType {

  /// 動かない
  none,

  /// 呼吸
  breathing,

  /// 左右揺れ
  wobble,

  /// だるそう上下
  tired,

  /// 小ジャンプ
  bounce,

  /// 浮遊
  floating,

  /// たまにビクッ
  twitch,
}